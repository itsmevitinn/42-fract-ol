int	ft_strcmp(char *first, char *second)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while(first[j])
	{
		if (first[i] == second[j])
		{
			i++;
			j++;
		}
		else
			return (1);
	}
	return (0);
}
